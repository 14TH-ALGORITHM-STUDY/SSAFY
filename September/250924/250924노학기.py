import sys
from collections import deque

input = sys.stdin.readline

m, n = map(int, input().split())
miro = [list(input().strip()) for _ in range(n)]

# ê° ì¹¸ê¹Œì§€ì˜ ìµœì†Œ ë¹„ìš©(ë¶€ìˆœ ë²½ì˜ ê°œìˆ˜)ì„ ì €ì¥í•  ë°°ì—´
# ì´ˆê¹ƒê°’ì€ ë§¤ìš° í° ìˆ˜ë¡œ ì„¤ì •í•˜ì—¬ ë°©ë¬¸í•˜ì§€ ì•Šì•˜ìŒì„ í‘œì‹œ
dist = [[-1] * m for _ in range(n)]

q = deque()

# ì‹œì‘ì  (0, 0) ì„¤ì •
q.append((0, 0))
dist[0][0] = 0
# ìƒ, í•˜, ì¢Œ, ìš°
dx = [-1, 1, 0, 0]
dy = [0, 0, -1, 1]

while q:
    x, y = q.popleft()

    for i in range(4):
        nx, ny = x + dx[i], y + dy[i]

        if 0 <= nx < n and 0 <= ny < m:
            if dist[nx][ny] == -1:
                # ë‹¤ìŒ ì¹¸ì´ ë¹ˆ ë°©ì¼ ê²½ìš° (ë¹„ìš© 0)
                if miro[nx][ny] == '0':
                    # ë¹„ìš©ì´ ë“¤ì§€ ì•Šìœ¼ë¯€ë¡œ ë±ì˜ ë§¨ ì•ì— ì¶”ê°€ (ìš°ì„  íƒìƒ‰)
                    q.appendleft((nx, ny))
                    dist[nx][ny] = dist[x][y]

                # ë‹¤ìŒ ì¹¸ì´ ë²½ì¼ ê²½ìš° (ë¹„ìš© 1)
                else:
                    # ë¹„ìš©ì´ ë°œìƒí•˜ë¯€ë¡œ ë±ì˜ ë§¨ ë’¤ì— ì¶”ê°€
                    q.append((nx, ny))
                    dist[nx][ny] = dist[x][y] + 1

print(dist[n-1][m-1])